public class tugas6 {
    public static void main(String[] args) {
        int a = 10;
        int b = 8;
        int c = 6;
        int d = 15;

        System.out.println("Nilai a = " + a++);//Hasilnya Nilai a = 10
        System.out.println("Nilai b = " + ++b);//Hasilnya Nilai b = 9
        System.out.println("Nilai c = " + c--);//Hasilnya Nilai c = 6
        System.out.println("Nilai d = " + --d);//Hasilnya Nilai d = 14
    }
}
